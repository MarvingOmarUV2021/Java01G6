public class EjemJava10 
{
    public static void main( String[] args )
    {
        byte num;
        System.out.println("\n\tLista de todos los valores de num que es tipo BYTE");
        for (num = -128 ; num <= 126 ; num++)
        {
            System.out.println("\tEl valor de num es: " + num );
        }
        System.out.println("\tEl valor de num es: " + num );
    }
}
